package baek;

import java.io.BufferedWriter;
import java.io.IOException;
import java.io.OutputStreamWriter;
import java.util.Scanner;

public class 재귀함수가_뭔가요 {
	public static String[] ment = {"\"재귀함수가 뭔가요?\"","\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.",
			"마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.","그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\""};
	public static String[] endingment = {"\"재귀함수가 뭔가요?\"","\"재귀함수는 자기 자신을 호출하는 함수라네\"","라고 답변하였지."};
	public static void main(String[] args) throws IOException {
		Scanner sc = new Scanner(System.in);
		BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
		int N = sc.nextInt();
		recursive(0,N,bw);
		bw.flush();
		bw.close();
	}
	public static void recursive(int start,int end,BufferedWriter bw) throws IOException {
		if(start==0) {
			bw.write("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.");
			bw.newLine();
		}else if(start==end) {
			
			for(int i=0;i<endingment.length;i++) {
				for(int j=0;j<end*4;j++) {
					bw.write("_");
				}
				bw.write(endingment[i]);
				bw.newLine();
			}
			
			return;
		}
		
		for(int i=0;i<ment.length;i++) {
			for(int j=0;j<start*4;j++) {
				bw.write("_");
			}
			bw.write(ment[i]);
			bw.newLine();
		}
		
		recursive(start+1,end,bw);
		for(int i=0;i<start*4;i++) {
			bw.write("_");
		}
		bw.write("라고 답변하였지.");
		bw.newLine();
	}
}
